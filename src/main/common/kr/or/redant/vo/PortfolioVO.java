package kr.or.redant.vo;
//포트폴리오
public class PortfolioVO {
	private String portfolio_code;		//포트폴리오 코드
	private String portfolio_name;		//포트폴리오 이름
	private String portfolio_title;
	private String portfolio_content;	//포트폴리오 내용
	private String portfolio_status;
	private String portfolio_rnum;
	
	public String getPortfolio_code() {
		return portfolio_code;
	}
	public void setPortfolio_code(String portfolio_code) {
		this.portfolio_code = portfolio_code;
	}
	public String getPortfolio_name() {
		return portfolio_name;
	}
	public void setPortfolio_name(String portfolio_name) {
		this.portfolio_name = portfolio_name;
	}
	public String getPortfolio_title() {
		return portfolio_title;
	}
	public void setPortfolio_title(String portfolio_title) {
		this.portfolio_title = portfolio_title;
	}
	public String getPortfolio_content() {
		return portfolio_content;
	}
	public void setPortfolio_content(String portfolio_content) {
		this.portfolio_content = portfolio_content;
	}
	public String getPortfolio_status() {
		return portfolio_status;
	}
	public void setPortfolio_status(String portfolio_status) {
		this.portfolio_status = portfolio_status;
	}
	public String getPortfolio_rnum() {
		return portfolio_rnum;
	}
	public void setPortfolio_rnum(String portfolio_rnum) {
		this.portfolio_rnum = portfolio_rnum;
	}
	
	
	
}
